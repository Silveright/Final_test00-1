package com.project.test.domain;

public class UserGroup {
	private String group_no;
	private String group_name;
	private String group_content;
	private String catename;
	private String group_user;
	private String group_role;
	private String group_img;
	public String getGroup_img() {
		return group_img;
	}
	public void setGroup_img(String group_img) {
		this.group_img = group_img;
	}
	public String getGroup_no() {
		return group_no;
	}
	public void setGroup_no(String group_no) {
		this.group_no = group_no;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public String getGroup_content() {
		return group_content;
	}
	public void setGroup_content(String group_content) {
		this.group_content = group_content;
	}
	public String getCatename() {
		return catename;
	}
	public void setCatename(String catename) {
		this.catename = catename;
	}
	public String getGroup_user() {
		return group_user;
	}
	public void setGroup_user(String group_user) {
		this.group_user = group_user;
	}
	public String getGroup_role() {
		return group_role;
	}
	public void setGroup_role(String group_role) {
		this.group_role = group_role;
	}
}
